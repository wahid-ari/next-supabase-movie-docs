create table
  public.movies (
    id bigint generated by default as identity,
    created_at timestamp with time zone null default now(),
    name text null,
    description text null,
    image_url text null,
    video_url text null,
    release_date date null,
    language text null,
    status smallint null,
    director_id bigint null,
    studio_id bigint null,
    backdrop_url text null,
    constraint movies_pkey primary key (id),
    constraint movies_director_id_fkey foreign key (director_id) references directors (id),
    constraint movies_studio_id_fkey foreign key (studio_id) references studios (id)
  ) tablespace pg_default;

