create table
  public.movie_actors (
    id bigint generated by default as identity,
    movie_id bigint null,
    actor_id bigint null,
    created_at timestamp with time zone null default now(),
    constraint movie_actors_pkey primary key (id),
    constraint movie_actors_actor_id_fkey foreign key (actor_id) references actors (id),
    constraint movie_actors_movie_id_fkey foreign key (movie_id) references movies (id)
  ) tablespace pg_default;

